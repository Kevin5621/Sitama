class Student {
  final String id;
  final String name;
  final String email;
  final String studentId;
  final String industry;
  final DateTime startDate;
  final DateTime endDate;

  Student({
    required this.id,
    required this.name,
    required this.email,
    required this.studentId,
    required this.industry,
    required this.startDate,
    required this.endDate,
  });
}